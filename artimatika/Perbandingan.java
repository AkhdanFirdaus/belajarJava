public class Perbandingan {
    public static void main(String[] args) {
        int x = 10, y = 20;
        System.out.println("X==Y :" + (x == y));
        System.out.println("X!=Y  :" + (x != y));
        System.out.println("X>Y :" + (x > y));
        System.out.println("X<=Y :" + (x <= y));
        System.out.println("X>+Y :" + (x >= y));
    }
}
