public class NomorTiga {
    public static void main(String[] args) {
        int number = 10;
        char letter = 'a';
        Boolean result = true;
        String str = "hello";
        System.out.println("Number\t= " + number);
        System.out.println("Letter\t= " + letter);
        System.out.println("Result\t= " + result);
        System.out.println("Str\t= " + str);
    }
}
